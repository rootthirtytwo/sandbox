# A partially filled sudoku is valid or not (9 x 9).
data = [
  ["5","3",".",".","7",".",".",".","."],
  ["6",".",".","1","9","5",".",".","."],
  [".","9","8",".",".",".",".","6","."],
  ["8",".",".",".","6",".",".",".","3"],
  ["4",".",".","8",".","3",".",".","1"],
  ["7",".",".",".","2",".",".",".","6"],
  [".","6",".",".",".",".","2","8","."],
  [".",".",".","4","1","9",".",".","5"],
  [".",".",".",".","8",".",".","7","9"]
]

status = True

for i in range(0,9,3):
    mat = list(zip(*data[i:i+3]))

    for j in range(3):
        lst = [v for i in mat[j:j + 3] for v in i if v.isdigit()]
        if len(lst) != len(set(lst)):
            status = False
            break

print(status)
